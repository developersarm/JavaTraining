package com.abhay;

public class LiteralsDemo {
	public static void main(String args[]) {
		int x = 0b1010;
		System.out.println(x);
		
		x = 073;
		System.out.println(x);

		x = 0x23b;
		System.out.println(x);

		x = 123_456_789;
		System.out.println(x);

		double y = 0x12.2P2;
		System.out.println(y);

		char z = '\ua432';
		System.out.println(z); 
	}
}
